package ve.com.digitel.notificationsender.util;

import java.io.Serializable;

public class StructCache implements Serializable {
	
	/**
	 * Version Serial de la Clase
	 */
	private static final long serialVersionUID = 14L;
	
	private String code;
	
	private String message;
	
	private String destinatary;
	
	private String noticeType;
	
	private String subject;

	
	/**
	 *  Constructor
	 */
	public StructCache() {
		super();		
	}



	/**
	 * @param code
	 * @param message
	 * @param destinatary
	 * @param noticeType
	 * @param subject
	 */
	public StructCache(String code, String message, String destinatary, String noticeType, String subject) {
		super();
		this.code = code;
		this.message = message;
		this.destinatary = destinatary;
		this.noticeType = noticeType;
		this.subject = subject;
	}



	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}

	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}

	/**
	 * @return the destinatary
	 */
	public String getDestinatary() {
		return destinatary;
	}

	/**
	 * @param destinatary the destinatary to set
	 */
	public void setDestinatary(String destinatary) {
		this.destinatary = destinatary;
	}

	/**
	 * @return the noticeType
	 */
	public String getNoticeType() {
		return noticeType;
	}

	/**
	 * @param noticeType the noticeType to set
	 */
	public void setNoticeType(String noticeType) {
		this.noticeType = noticeType;
	}

	/**
	 * @return the subject
	 */
	public String getSubject() {
		return subject;
	}

	/**
	 * @param subject the subject to set
	 */
	public void setSubject(String subject) {
		this.subject = subject;
	}
	
	
}
